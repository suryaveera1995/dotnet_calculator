using System;

namespace consoleApp
{
    public class Calculator
    {
        public int Sum(int a, int b)
        {
            return a+b;
        }
        public int Diff(int a, int b)
        {
            return a-b;
        }
        public int Divi(int a, int b)
        {
            return a/b;
        }
        public int Mul(int a, int b)
        {
            return a*b;
        }
    }
}
